class RRDBNet(nn.Module):
    def __init__(self, in_nc, out_nc, nf, nb, gc=32, upscale=4, norm_type=None, \
            act_type='leakyrelu', mode='CNA', upsample_mode='upconv',
                 upsample_kernel_mode="nearest", no_conv=False, last_act=None):
        super(RRDBNet, self).__init__()
        n_upscale = int(math.log(upscale, 2))
        if upscale == 3:
            n_upscale = 1

        fea_conv = B.conv_block(in_nc, nf, kernel_size=3, norm_type=None, act_type=None)

        rb_blocks = [B.RRDB(nf, kernel_size=3, gc=gc, stride=1, bias=True, pad_type='zero', \
                            norm_type=norm_type, act_type=act_type, mode='CNA') for _ in
                     range(nb)]

        LR_conv = B.conv_block(nf, nf, kernel_size=3, norm_type=norm_type, act_type=None, mode=mode)

        if upsample_mode == 'upconv':
            upsample_block = B.upconv_blcok
        elif upsample_mode == 'pixelshuffle':
            upsample_block = B.pixelshuffle_block
        else:
            raise NotImplementedError('upsample mode [{:s}] is not found'.format(upsample_mode))
        if upscale == 3:
            upsampler = upsample_block(nf, nf, 3, act_type=act_type, mode=upsample_kernel_mode)
        else:
            if no_conv:
                upsampler = [nn.Upsample(scale_factor=upscale, mode=upsample_kernel_mode)]
            else:
                upsampler = [upsample_block(nf, nf, act_type=act_type, mode=upsample_kernel_mode) for _ in
                             range(n_upscale)]

        HR_conv0 = B.conv_block(nf, nf, kernel_size=3, norm_type=None, act_type=act_type)
        HR_conv1 = B.conv_block(nf, out_nc, kernel_size=3, norm_type=None, act_type=last_act)

        self.last_act = last_act

        self.model = B.sequential(fea_conv, B.ShortcutBlock(B.sequential(*rb_blocks, LR_conv)),\
            *upsampler, HR_conv0, HR_conv1)

    def forward(self, x, code=None):
        result = self.model(x)
        if self.last_act == "tanh":
            new_result = result + 1
            new_result /= 2.
            result = new_result
        return result


class IMRRDBNet(nn.Module):
    def __init__(self, in_nc, in_code_nc, out_nc, nf, nb, gc=32, upscale=4, norm_type=None, \
            act_type='leakyrelu', mode='CNA', upsample_mode='upconv',
                 upsample_kernel_mode="nearest", no_conv=False, use_sigmoid=False, last_act=None):
        super(IMRRDBNet, self).__init__()
        n_upscale = int(math.log(upscale, 2))

        fea_conv = B.conv_block(in_nc + in_code_nc, nf, kernel_size=3, norm_type=None, act_type=None)
        rb_blocks = [B.RRDB(nf, kernel_size=3, gc=gc, stride=1, bias=True, pad_type='zero', \
            norm_type=norm_type, act_type=act_type, mode='CNA') for _ in range(nb)]
        LR_conv = B.conv_block(nf, nf, kernel_size=3, norm_type=norm_type, act_type=None, mode=mode)

        if upsample_mode == 'upconv':
            upsample_block = B.upconv_blcok
        elif upsample_mode == 'pixelshuffle':
            upsample_block = B.pixelshuffle_block
        else:
            raise NotImplementedError('upsample mode [{:s}] is not found'.format(upsample_mode))
        if no_conv:
            upsampler = [nn.Upsample(scale_factor=upscale, mode=upsample_kernel_mode)]
        else:
            upsampler = [upsample_block(nf, nf, act_type=act_type, mode=upsample_kernel_mode) for _ in
                         range(n_upscale)]

        HR_conv0 = B.conv_block(nf, nf, kernel_size=3, norm_type=None, act_type=act_type)

        if use_sigmoid:
            HR_conv1 = B.conv_block(nf, out_nc, kernel_size=3, norm_type=None, act_type="sigmoid")
        else:
            HR_conv1 = B.conv_block(nf, out_nc, kernel_size=3, norm_type=None, act_type=last_act)

        self.last_act = last_act

        self.model = B.sequential(fea_conv, B.ShortcutBlock(B.sequential(*rb_blocks, LR_conv)), \
                                  *upsampler, HR_conv0, HR_conv1)

    def forward(self, img, code):
        x = torch.cat((img, code), dim=1)
        result = self.model(x)
        if self.last_act == "tanh":
            new_result = result + 1
            new_result /= 2.
            result = new_result
        return result


class CaffeTwoStackNet(nn.Module):
    def __init__(self, in_nc, in_code_nc, gc, norm_type=None, act_type='leakyrelu', kernel_size=5, use_wn=False):
        super(CaffeTwoStackNet, self).__init__()
        # ==========
        # Caffe SRIM
        # ==========
        self.upconv_0 = nn.Upsample(scale_factor=2, mode="bilinear")
        if use_wn:
            self.c_conv_0 = B.WN_SkipBlock_9C(in_nc, in_code_nc, kernel_size=kernel_size, gc=gc, act_type=act_type)
        else:
            self.c_conv_0 = B.SkipBlock_9C(in_nc, in_code_nc, kernel_size=kernel_size, gc=gc, norm_type=norm_type,
                                       act_type=act_type)

        self.upconv_1 = nn.Upsample(scale_factor=2, mode="bilinear")
        if use_wn:
            self.c_conv_1 = B.WN_SkipBlock_9C(in_nc, in_code_nc, kernel_size=kernel_size, gc=gc, act_type=act_type)
        else:
            self.c_conv_1 = B.SkipBlock_9C(in_nc, in_code_nc, kernel_size=kernel_size, gc=gc, norm_type=norm_type,
                                       act_type=act_type)

    def forward(self, img, code, code_1, code_2=None):
        # ==========
        # Caffe SRIM
        # ==========

        upsample_lr = self.upconv_0(img)
        output_0 = self.c_conv_0(upsample_lr, code)
        upsample_lr = self.upconv_1(upsample_lr)
        output_1 = self.upconv_1(output_0)
        output_1 = self.c_conv_1(output_1, code_1, upsample_lr)

        return [output_0, output_1]


class CaffeThreeStackNet(nn.Module):
    def __init__(self, in_nc, in_code_nc, gc, norm_type=None, act_type='leakyrelu', kernel_size=5, use_wn=False):
        super(CaffeThreeStackNet, self).__init__()
